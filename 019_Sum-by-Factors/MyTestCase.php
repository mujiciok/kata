<?php

class MyTestCase extends TestCase
{
    private function revTest($actual, $expected) {
        $this->assertSame($expected, $actual);
    }
    public function testBasics() {
        $this->revTest(sumOfDivided([-69,28,44,38,31,39,12,41,36,38,18,37,40,29,44,15,27,46]), [ [2, 344], [3, 78], [5, 55], [7, 28], [11, 88], [13, 39], [19, 76], [23, -23], [29, 29], [31, 31], [37, 37], [41, 41] ]);
        $this->revTest(sumOfDivided([32,-47,39,26,-71,43,28,-60,-56,44,19,27,38,38,39,10,34,47,45,20,40,31,50,14,44]), [ [2, 302], [3, 90], [5, 105], [7, -14], [11, 88], [13, 104], [17, 34], [19, 95], [31, 31], [43, 43], [47, 0], [71, -71] ]);

        $this->revTest(sumOfDivided([107,158,204,100,118,123,126,110,116,100]), [ [2, 1032], [3, 453], [5, 310], [7, 126], [11, 110], [17, 204], [29, 116], [41, 123], [59, 118], [79, 158], [107, 107] ]);
        $this->revTest(sumOfDivided([-29804,-4209,-28265,-72769,-31744]), [ [2, -61548], [3, -4209], [5, -28265], [23, -4209], [31, -31744], [53, -72769], [61, -4209], [1373, -72769], [5653, -28265], [7451, -29804] ]);

        $this->revTest(sumOfDivided([12, 15]), [ [2, 12], [3, 27], [5, 15] ]);
        $this->revTest(sumOfDivided([15,21,24,30,45]), [ [2, 54], [3, 135], [5, 90], [7, 21] ]);
        $this->revTest(sumOfDivided([15,21,24,30,-45]), [ [2, 54], [3, 45], [5, 0], [7, 21] ]);
    }
}
